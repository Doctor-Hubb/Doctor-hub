local function decode(data, key)
  local s = {}
  local kn = #key
  for i=1,#data do
    local b = data[i]
    local k = string.byte(key, ((i-1) % kn) + 1)
    s[i] = string.char(b ~ k)
  end
  return table.concat(s)
end

